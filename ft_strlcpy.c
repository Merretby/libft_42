#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	ln_s;

	i = 0;
	ln_s = ft_strlen(src);
	if (size == 0)
		return (ln_s);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ln_s);
}
